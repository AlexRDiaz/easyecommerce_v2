import 'package:flutter/material.dart';
import 'package:flutter_html/flutter_html.dart';

class TextStylesSystem {
  
  TextStyle ralewayStyle(fontSizeP, fontWeightP, colorP) {
    return TextStyle(
        fontFamily: 'Raleway',
        fontSize: fontSizeP,
        fontWeight: fontWeightP,
        color: colorP);
  }

}
